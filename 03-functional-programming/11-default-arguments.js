/** 
 * average 関数
 * 2つの引数の平均値を計算する関数。
 * 引数が省略された場合、デフォルト値が設定される。
 * 
 * @param {number} x - 最初の数値。デフォルトは 0。
 * @param {number} y - 2番目の数値。デフォルトは x の値と同じ。
 * @returns {number} - 引数 x と y の平均値
 */
const average = (x = 0, y = x) => (x + y) / 2;

console.log(average(3));    // 3 (x=3, y=3 → (3+3)/2)
console.log(average());     // 0 (x=0, y=0 → (0+0)/2)
console.log(average(undefined, 3)); // 1.5 (x=0, y=3 → (0+3)/2)

